package pkg4;

public class ClassC {

	public static void main(String[] args) {
		
		//star patterns
		
		for(int i=0; i<=5; i++)
			
		 {
			
			for(int j=0; j<=5; j++)
			{
				System.out.print("* ");
			}
			System.out.println();
		}
		
		for( int a=1; a<=6; a++)
		{
			for(int b=1; b<=a; b++)
			{
				System.out.print("* ");
		    }
			System.out.println();
	    }
		
	
		
		for(int m=1; m<=5; m++)
		{
		    for(int n=5; n>=m; n--)
		    {
		    	System.out.print("* ");
		    }
		    System.out.println();
		}
		
		for(int a=1; a<=5; a++)
		{
			for(int b=1; b<=5; b++)
		{
			System.out.print("* ");
		}
			System.out.println();
		}
		
		
		
}
	}
